    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $validated = $request->validate(['task' => 'required|min:5']);

        $task = new Task;

        $task->taskname  = $validated['task'];

        $task->save();

        return redirect('/');
